#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function parseEnv(content) {
  /** @type {Record<string, string>} */
  const out = {};
  for (const line of content.split("\n")) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const i = t.indexOf("=");
    if (i === -1) continue;
    const key = t.slice(0, i).trim();
    let val = t.slice(i + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

const arg = process.argv[2];
const envRel =
  arg ??
  (fs.existsSync(path.join(root, ".env")) ? ".env" : ".env.example");
const envPath = path.isAbsolute(envRel) ? envRel : path.join(root, envRel);

if (!fs.existsSync(envPath)) {
  console.error(`Env file not found: ${envPath}`);
  process.exit(1);
}

const env = parseEnv(fs.readFileSync(envPath, "utf8"));
const url = env.EXTENSION_API_BASE_URL?.trim();
if (!url) {
  console.error(`Missing EXTENSION_API_BASE_URL in ${envPath}`);
  process.exit(1);
}

let parsed;
try {
  parsed = new URL(url);
} catch {
  console.error(`Invalid EXTENSION_API_BASE_URL: ${url}`);
  process.exit(1);
}

if (parsed.protocol !== "http:" && parsed.protocol !== "https:") {
  console.error(`EXTENSION_API_BASE_URL must be http(s): ${url}`);
  process.exit(1);
}

const outPath = path.join(root, "src/lib/extension-api-base.const.ts");
const body = `/** Generated by scripts/sync-extension-api-base.mjs — do not edit. */
export const EXTENSION_API_BASE_URL = ${JSON.stringify(url.replace(/\/$/, ""))};
`;
fs.writeFileSync(outPath, body, "utf8");
console.log(`Wrote ${path.relative(root, outPath)} from ${path.relative(root, envPath)}`);
